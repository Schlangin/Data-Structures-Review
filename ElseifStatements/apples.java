package ElseifStatements;

public class apples {
	public static void main(String[] args) {
		int age = 30;
		
		if(age >= 60)
			System.out.println("youre a senior citizen");
		else if (age >= 50)
			System.out.println("you are in your 50s");
		else if (age >= 40)
			System.out.println("you are in your 40s");
		else 
			System.out.println("youre a yung buck");
	}
}
